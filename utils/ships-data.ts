function getAllDestinations(): string[] {
  let destinations: any[] = [];

  for(let i of data) {
    const currentDestinations = i.destinations;

    for(let y of currentDestinations) {
      if(destinations.includes(y)) continue;

      destinations.push(y);
    }
  }

  return destinations;
}

function getListOfBuildYears() {

  let buildYear: number[] = [];

  for(let i of data) {
    let yearBuilt = i.yearBuilt;
    // console.log(yearBuilt);

    if(yearBuilt === 'TBD') {
      const currentTime = new Date();
      yearBuilt = currentTime.getFullYear().toString();
    }

    if(buildYear.includes(Number(yearBuilt))) continue;

    buildYear.push(Number(yearBuilt))
  }

  return buildYear.sort()

}

